"Micaela no quiso entregar un celular y le dijeron ella es «cobani» y le pegaron a secas un disparo en el pecho, la reconocieron", dijo Sara, una tía de la víctima, al canal Todo Noticias durante una manifestación realizada esta mañana.<br><br>
De acuerdo a los voceros, la joven fue trasladada en grave estado por un grupo de vecinos al Hospital El Cruce de Florencio Varela, donde ingresó inconsciente y falleció a los pocos minutos producto de la herida recibida.<br><br>
<strong>El sueño de ser policía</strong><br><br>
La oficial Romero tenía un año de antigüedad en la fuerza y era integrante de la División Protección Residencia Presidencial de Olivos y Operaciones, donde actualmente estaba de licencia médica. Además, la joven era madre de una niña de 2 años, señalaron las fuentes.
"El sueño de ella era ser policía, el año pasado se graduó, tenía una hija de 2 años y siete hermanos. Era una excelente persona, todo lo que tenía lo tuvo con el esfuerzo y apoyo de su familia", destacó la mujer, quien agregó: ¿Cómo le decimos a mi sobrinita que la madre ya no está?»", sostuvo Natalia, otra tía de la víctima.
